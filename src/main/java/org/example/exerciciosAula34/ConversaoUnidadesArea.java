package org.example.exerciciosAula34;

public class ConversaoUnidadesArea {
    public static double metroQuadPPesQuad (double metro){
        return metro * 10.76;
    }

    public static double peQuadPCentQuad (double peQuadrado){
        return peQuadrado * 929;
    }

    public static double milhaQuadPAcres (double milha) {
        return milha * 640;
    }
    public static double acrePPeQuad (double acre) {
        return acre * 43560;
    }
}
